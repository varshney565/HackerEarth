const int mod = 1e9+7;
 
int power(int a,int b) {
    if(b == 0) {
        return 1;
    } 
    int smallAns = power(a,b/2);
    int ans = ((long long)smallAns*smallAns)%mod;
    if(b%2) {
        ans = ((long long)ans*a)%mod;
    }
    return ans;
}
 
pair<int,int> go(int a,int b) {
    if(a%b == 0) {
        return {power(2,a-1),power(2,b-1)};
    }
    auto res = go(b,a%b);
    int x = res.first;
    int y = res.second;
    int n = a/b;
    int r = power(2,b);
    int ans = ((((long long)x*((power(r,n)-1+mod)%mod))%mod)*power((r-1+mod)%mod,mod-2))%mod;
    ans = (((long long)ans*power(2,a%b))%mod+y)%mod;
    return {ans,x};
};

