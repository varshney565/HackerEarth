void solve() {
	int n;
	cin>>n;
	vector<int> v(n);
	read(n,v);
	vector<int> cnt(n,-1);
	set<int> missing;
	for(int i = 1 ; i <= n ; i++) {
		if(i < n) {
			if(v[i] < v[i-1]) {
				cout<<"0\n";
				return;
			}
		}
		missing.insert(i);
	}

	map<int,int> index;
	for(int i = 0 ; i < n ; i++) {
		if(i == 0 || v[i] != v[i-1]) {
			missing.erase(v[i]);
			cnt[i] = v[i];
		}
	}
	int j = 0;
	for(auto i = missing.begin() ; i != missing.end() ; i++) {
		index[*i] = j++;
	}

	debug(index);
	debug(missing);
	debug(cnt);
	int ans = 1,val = 0,prev = v[0];
	const int mod = 1e9+7;
	for(int i = 1 ; i < n ; i++) {
		if(cnt[i] == -1) {
			int small = -val;
			auto it = missing.upper_bound(prev);
			int ind = missing.size();
			if(it != missing.end()) {
				ind = index[*it];
			}
			small += ind;
			if(small <= 0) {
				cout<<"0\n";
				return;
			}
			ans = (ans*(long long)small)%mod;
			val++;
		}else {
			prev = cnt[i];
		}
	}
	cout<<ans<<"\n";
}

